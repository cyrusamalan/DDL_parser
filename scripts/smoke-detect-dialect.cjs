const assert = require("node:assert/strict");

function mergeSqlFiles(files) {
  return files
    .map((file) => file.sql.trim())
    .filter(Boolean)
    .join("\n\n");
}

function detectDialect(sql) {
  if (
    /`[^`]+\.[^`]+\.[^`]+`/.test(sql) ||
    /\bSTRUCT\s*</i.test(sql) ||
    /\bARRAY\s*</i.test(sql)
  ) {
    return "bigquery";
  }
  if (/`/.test(sql) || /\bENGINE\s*=/i.test(sql) || /\bAUTO_INCREMENT\b/i.test(sql)) {
    return "mysql";
  }
  if (
    /\[[A-Za-z_]\w*\]/.test(sql) ||
    /\bIDENTITY\s*\(/i.test(sql) ||
    /^GO\s*$/im.test(sql) ||
    /\bNVARCHAR\b/i.test(sql)
  ) {
    return "mssql";
  }
  if (/\bAUTOINCREMENT\b/i.test(sql) || /\bWITHOUT\s+ROWID\b/i.test(sql)) {
    return "sqlite";
  }
  return "postgresql";
}

function sqlForDialectDetection(sql, files) {
  if (files.length > 0) {
    return mergeSqlFiles(files);
  }
  return sql;
}

function detectDialectFromInput(sql, files) {
  return detectDialect(sqlForDialectDetection(sql, files));
}

const mysqlSql = `CREATE TABLE \`users\` (
  \`id\` INT AUTO_INCREMENT PRIMARY KEY,
  \`email\` VARCHAR(255) NOT NULL
) ENGINE=InnoDB;`;

const pgSql = `CREATE TABLE users (
  id SERIAL PRIMARY KEY,
  email VARCHAR(255) NOT NULL UNIQUE
);`;

const sqliteSql = `CREATE TABLE users (
  id INTEGER PRIMARY KEY AUTOINCREMENT,
  email TEXT NOT NULL
) WITHOUT ROWID;`;

const files = [{ id: "f1", name: "schema.sql", sql: mysqlSql }];

assert.equal(detectDialect(mysqlSql), "mysql");
assert.equal(detectDialect(pgSql), "postgresql");
assert.equal(detectDialect(sqliteSql), "sqlite");

assert.equal(sqlForDialectDetection("", files), mysqlSql.trim());
assert.equal(sqlForDialectDetection(pgSql, files), mysqlSql.trim());

assert.equal(detectDialectFromInput("", files), "mysql");
assert.equal(detectDialectFromInput(pgSql, []), "postgresql");
assert.equal(detectDialectFromInput("", []), "postgresql");

const pgIdentitySql = `CREATE TABLE users (
  id INT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY
);`;
assert.equal(detectDialect(pgIdentitySql), "postgresql");

console.log("Dialect detection smoke test passed.");
